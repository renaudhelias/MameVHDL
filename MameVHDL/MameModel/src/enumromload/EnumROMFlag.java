	/**
	 * 
	 * @param flags separated by |
	 * @return
	 */
	public static List<EnumROMFlag> construct(String flags) {
		StringTokenizer st = new StringTokenizer(flags,"|");
		List<EnumROMFlag> flagsResult = new ArrayList<EnumROMFlag>();
		while (st.hasMoreTokens()) {
			String token = st.nextToken();
			if (token.startsWith("ROM_GROUPSIZE(")) {
				token.replace("ROM_GROUPSIZE(", "");
				token.replace(")", "");
				int n = Integer.valueOf(token);
				flagsResult.add(ROM_GROUPSIZE(n));
			} else {
				token.replace("(", "");
				token.replace(")", "");
				if (token.equals("0")) {
					continue;
				} else {
					EnumROMFlag s = EnumROMFlag.valueOf(token);
					flagsResult.add(s);
				}
			}
		}
		return flagsResult;
	}
	
	
	public static List<EnumROMFlag> verbose(List<EnumROMFlag> clean) {
		List<EnumROMFlag> flags = new ArrayList<EnumROMFlag>();
		for (EnumROMMask mask : EnumROMMask.values()) {
			boolean maskUsed = false;
			for (EnumROMFlag flag : clean) {
				if (flag.getMask() == mask) {
					flags.add(flag);
					maskUsed = true;
					break;
				}
			}
			if (!maskUsed) {
				for (EnumROMFlag flag : EnumROMFlag.values()) {
					if (flag.getMask() == mask && flag.getHexa() == 0) {
						flags.add(flag);
						break;
					}
				}
			}
		}
		return flags;
	}
	
