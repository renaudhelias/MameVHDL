package enumromload;

public enum EnumROMMask {
	ROMENTRY_TYPEMASK(0x0000000f),
	ROMREGION_WIDTHMASK(0x00000300),
	ROMREGION_ENDIANMASK(0x00000400),
	ROMREGION_INVERTMASK(0x00000800),
	ROMREGION_ERASEMASK(0x00002000),
	ROMREGION_DATATYPEMASK(0x00004000),
	ROMREGION_ERASEVALMASK(0x00ff0000),
	DISK_READONLYMASK(0x00000010),
	ROM_OPTIONALMASK(0x00000020),
	ROM_REVERSEMASK(0x00000040),
	ROM_INHERITFLAGSMASK(0x00000080),
	ROM_GROUPMASK(0x00000f00),
	ROM_SKIPMASK(0x0000f000),
	ROM_BITWIDTHMASK(0x000f0000),
	ROM_BITSHIFTMASK(0x00f00000),
	ROM_BIOSFLAGSMASK(0xff000000),
	ROM_INHERITEDFLAGS(ROM_GROUPMASK.getHexa() | ROM_SKIPMASK.getHexa() | ROM_REVERSEMASK.getHexa() | ROM_BITWIDTHMASK.getHexa() | ROM_BITSHIFTMASK.getHexa() | ROM_BIOSFLAGSMASK.getHexa());
	
	private int hexa = 0;
	EnumROMMask(int hexa) {
		this.hexa = hexa;
	}
	public int getHexa() {
		return hexa;
	}
}
